// Return type of variables in JavaScript
// 1) Primitive Datatypes
//        Number => number
//        String  => string
//        Boolean  => boolean
//        null  => object
//        undefined  =>  undefined
//        Symbol  =>  symbol: unique identifier
//        BigInt  =>  bigint

// 2) Non-primitive Datatypes
//        Arrays  =>  object: Stores ordered list of values
//        Function  =>  function: A block of reusable code
//        Object  =>  object: Stores key–value pairs
//        Date, Map, Set, etc: Special object type

// A data type tells JavaScript what kind of value you are storing.


// Primitive = single, simple value
// They:
// store only one value
// are immutable (cannot be changed, only replaced)
// are copied by value
// Primitive = value itself is stored
// They are simple values, not objects
// JS treats them as read-only

// Immutable means you can’t change the value itself — you can only replace it with a new one.

// Non-primitive = collection of values
// They:
// can store multiple values
// are mutable (can be changed)
// are copied by reference

